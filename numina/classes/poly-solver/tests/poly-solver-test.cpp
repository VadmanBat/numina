using Type    = double;
using Complex = std::complex<Type>;

// Horner scheme (high-to-low: coeffs[0] — старшая степень)
Type evaluate_poly(const std::vector<Type>& coeffs, Complex z) {
    if (coeffs.empty())
        return 0.0;
    Complex result = coeffs[0];
    for (size_t i = 1; i < coeffs.size(); ++i)
        result = result * z + coeffs[i];
    return std::abs(result);
}

struct TestCase {
    std::string name;
    std::vector<Type> coeffs; // high-to-low
};

void run_test(const TestCase& tc, numina::PolySolver& solver) {
    auto roots = solver.solveWithImplicitDeflation(tc.coeffs);

    double sum_error = 0.0;
    double max_error = 0.0;
    int total_roots  = 0;

    for (const auto& [r, mult] : roots.first) {
        double err = evaluate_poly(tc.coeffs, r);
        sum_error  += err;
        max_error  = std::max(max_error, err);
        total_roots++;
    }
    for (const auto& [r, mult] : roots.second) {
        double err = evaluate_poly(tc.coeffs, r);
        sum_error  += err;
        max_error  = std::max(max_error, err);
        total_roots++;
    }

    double avg_error = (total_roots > 0) ? (sum_error / total_roots) : 0.0;

    std::cout << std::left << std::setw(38) << tc.name
        << "  Max error: " << std::scientific << std::setprecision(2) << max_error
        << "   Avg error: " << std::scientific << std::setprecision(2) << avg_error
        << std::endl;
}

